package at.ac.tgm.rebay.rebay_backend.models;

/**
 * Enum zur Definition der Produktkategorien, um eine standardisierte Klassifizierung zu gewährleisten.
 * Die Kategorien umfassen allgemeine Gruppen von Geräten auf der Plattform, wie COMPUTER und MONITOR.
 */
public enum CategoryEnum {
    MONITOR,            // Bildschirmgeräte wie Monitore
    COMPUTER,           // Hauptsysteme wie Desktop-PCs und Laptops
    COMPUTERZUSATZ,     // Zubehör wie Tastaturen und Mäuse
    AUDIO,              // Audiogeräte wie Lautsprecher oder Kopfhörer
    DRUCKER;           // Kopiergeräte und Multifunktionsdrucker
}
